package com.student.bean;

public class StudentDetails {
	String fname;
	String lname;
	String gender;
	String dob;
	String pic;
	Long pmarks;
	Long cmarks;
	Long mmarks;
	String grade;
	String rollNo;
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public Long getPMarks() {
		return pmarks;
	}
	public Long getCMarks() {
		return cmarks;
	}
	public Long getMMarks() {
		return mmarks;
	}
	public String getGrade() {
		return grade;
	}
	public void setPMarks(Long pmarks) {
		this.pmarks = pmarks;
	}
	public void setCMarks(Long cmarks) {
		this.cmarks = cmarks;
	}
	public void setMMarks(Long mmarks) {
		this.mmarks = mmarks;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getRollNo() {
		return rollNo;
	}
	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}
}
